#include <iostream>

using namespace std;

int main(){
	bool a = false;
	bool b = true;

	cout << "a = " << a << endl;

	cout << "b = " << b << endl;

	cout << "a + b = " << a + b << endl;

	a = 1;

	b = 0;

	cout << "a = " << a << endl;
	cout << "b = " << b << endl;


	b = 100;

	cout << "b = " << b << endl;

}


